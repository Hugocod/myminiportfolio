// funzione per far apparire un popup aggiuntivo con funzione descrittiva

let wrapper = document.querySelector(".wrapper-post-body");
let triggers = document.querySelectorAll(".logos-eyelink");

triggers.forEach((element) => {
  element.addEventListener("click", (e) => {
    if (element.classList.contains("l-nhb")) {
      displayLogoPage(nhb, e);
    } else if (element.classList.contains("l-rm")) {
      displayLogoPage(rm, e);
    } else if (element.classList.contains("l-adv")) {
      displayLogoPage(adv, e);
    } else if (element.classList.contains("l-ch")) {
      displayLogoPage(ch, e);
    } else if (element.classList.contains("l-mw")) {
      displayLogoPage(mw, e);
    } else if (element.classList.contains("l-sp")) {
      displayLogoPage(sp, e);
    } else if (element.classList.contains("l-eh")) {
      displayLogoPage(eh, e);
    }
  });
});

function displayLogoPage(logoPage, e) {
  // disable the scroll of the body
  document.body.style.overflow = "hidden";

  // display popup
  document.body.insertAdjacentHTML(
    "afterbegin",
    `<div id="popup" class="single_logo"></div>`
  );

  // display the single-page-logo-description
  document.body.insertAdjacentHTML("afterbegin", logoPage);

  let logoSingle = document.getElementById("logo_single");
  let popup = document.getElementById("popup");

  // disable the scroll of the body

  document
    .querySelector(".single_logo")
    .setAttribute("style", `left:${e.clientX}px ; top:${e.clientY}px `);

  anime({
    targets: ".wrapper-solid",
    opacity: 1,
    duration: 300,
  });

  anime
    .timeline({
      easing: "easeOutExpo",
      duration: 800,
    })
    .add({
      targets: ".single_logo",
      width: "800vh",
      height: "800vh",
    })
    .add({
      targets: ".single_logo",
      opacity: [1, 0],
    })
    .add({
      targets: ".single_logo",
      width: "1px",
      height: "1px",
      transform: "translateY(100%)",
    });

  document.getElementById("exit").addEventListener("click", () => {
    document.body.removeChild(logoSingle);
    document.body.removeChild(popup);
    // enable the scroll of the body
    document.body.style.overflow = "scroll";
  });
}
